
/**
 * Programa que suma los primeros 100 numeros utilizando estructuras de control
 */

public class Estructuras {
    public static void main (String args []){
        int suma=0;
        int i;
        for (i=1; i<101; i++){
            suma = suma + i;
        }
        System.out.print(suma);
    }
 }
 